#include <iostream>
using namespace std;

int main()
{
    int dia = 1;
    
    switch (dia) {
        case 1:
        cout << "domingo";
        break;
        
        case 2:
        cout << "segunda";
        break;
        
        case 3:
        cout << "terca";
        break;
        
        case 4:
        cout << "quarta";
        break;
        
        case 5:
        cout << "quinta";
        break;
        
        case 6:
        cout << "sexta";
        break;
        
        case 7:
        cout << "sabado";
        break;
        
        default:
        cout << "digite um valor valido";
        
    }

}
